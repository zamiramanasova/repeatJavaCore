package com.company.lessonFourSwitch.leetCode;

/**
 * 709. toLowerCase
 */
public class ToLowerCase {
    public static void main(String[] args) {

        String s = "Hello";
        System.out.println(toLowerCase(s.toLowerCase()));
    }
    public static String toLowerCase(String s) {
        s.toLowerCase();
        return s;
    }
}
